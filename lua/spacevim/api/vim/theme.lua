function M.palette(name)
  name = name or vim.g.colors_name or 'gruvbox'
  return palettes[name] or palettes.gruvbox
end

function M.hi()
  local name = vim.g.colors_name or 'gruvbox'
  local t = palettes[name] or palettes.gruvbox
  M.apply(t)
end

function M.apply(t)
  vim.cmd('hi! LeaderGuiderPrompt ctermbg=' .. t[1][4] .. ' ctermfg=' .. t[1][3] .. ' cterm=bold gui=bold guifg=' .. t[1][1] .. ' guibg=' .. t[1][2])
  vim.cmd('hi! LeaderGuiderSep1 ctermbg=' .. t[2][4] .. ' ctermfg=' .. t[1][4] .. ' cterm=bold gui=bold guifg=' .. t[1][2] .. ' guibg=' .. t[2][2])
  vim.cmd('hi! LeaderGuiderName ctermbg=' .. t[2][4] .. ' ctermfg=' .. t[2][3] .. ' cterm=bold gui=bold guifg=' .. t[2][1] .. ' guibg=' .. t[2][2])
  vim.cmd('hi! LeaderGuiderSep2 ctermbg=' .. t[3][4] .. ' ctermfg=' .. t[2][4] .. ' cterm=bold gui=bold guifg=' .. t[2][2] .. ' guibg=' .. t[3][2])
  vim.cmd('hi! LeaderGuiderFill ctermbg=' .. t[3][4] .. ' ctermfg=' .. t[3][3] .. ' guifg=' .. t[3][1] .. ' guibg=' .. t[3][2])
end

return M

